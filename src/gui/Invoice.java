    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String pid = jLabel7.getText();
        String qty = jTextField3.getText();
        String selling_price = jLabel16.getText();
        
        if(pid.equals("None")){
            JOptionPane.showMessageDialog(this, "Please select a Product!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(qty.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter Quantity!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(!Pattern.compile("[1-9][0-9]*").matcher(qty).matches()){
            JOptionPane.showMessageDialog(this, "Invalid Quantity!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            
            boolean isFound = false;
            int x = -1;
            
            for(int i = 0; i < dtm.getRowCount(); i++){
                String id = jTable1.getValueAt(i, 0).toString();
                
                if(id.equals(pid)){
                    isFound = true;
                    x = i;
                    break;
                }
            }
            
            if(isFound){
                
                int option = JOptionPane.showConfirmDialog(this, "This Product is already Added. Do you want to Update?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                
                if(option == JOptionPane.YES_OPTION){
                    
                    int oldQty = Integer.parseInt(jTable1.getValueAt(x, 4).toString());
                    int fullQty = oldQty + Integer.parseInt(qty);
                    
                    jTable1.setValueAt(String.valueOf(fullQty), x, 4);
                    jTable1.setValueAt(selling_price, x, 5);
                    
                    double updatedItemTotal = fullQty * Double.parseDouble(selling_price);
                    jTable1.setValueAt(String.valueOf(updatedItemTotal), x, 6);
                    
                    fullTotal();
                    
                }
                
                jLabel5.setText("None");
                jLabel7.setText("None");
                jLabel9.setText("None");
                jLabel11.setText("None");
                jLabel13.setText("None");
                jTextField3.setText("");
                jLabel16.setText("0.00");
                
            }else{
                
                Vector v = new Vector();
                v.add(jLabel5.getText());
                v.add(pid);
                v.add(jLabel9.getText());
                v.add(jLabel11.getText());
                v.add(jLabel13.getText());
                v.add(qty);
                v.add(selling_price);

                double itemtotal = Integer.parseInt(qty) * Double.parseDouble(selling_price);
                v.add(df.format(itemtotal));

                dtm.addRow(v);                

                fullTotal();
                
                jLabel5.setText("None");
                jLabel7.setText("None");
                jLabel9.setText("None");
                jLabel11.setText("None");
                jLabel13.setText("None");
                jTextField3.setText("");
                jLabel16.setText("0.00");

                JOptionPane.showMessageDialog(this, "Product Added to the Invoice Successfully!!!", "Success", JOptionPane.INFORMATION_MESSAGE);
                
            }
  
        }
    }//GEN-LAST:event_jButton3ActionPerformed

