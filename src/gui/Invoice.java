    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String mobile = jTextField1.getText();
        String cname = jTextField2.getText();
        String city = jComboBox1.getSelectedItem().toString();
        
        if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Contact Number!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("07[01245678][0-9]{7}").matcher(mobile).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Contact Number!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (cname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Customer Name!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a City!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            
            try {
                
                ResultSet rs1 = DBConnection.search("SELECT * FROM `customer` WHERE `contact_number`='"+mobile+"';");
                
                ResultSet rs2 = DBConnection.search("SELECT `id` FROM `city` WHERE `name`='"+city+"';");
                rs2.next();
                String city_id = rs2.getString("id");
            
                if(rs1.next()) {

                    DBConnection.iud("UPDATE `customer` SET `name`='"+cname+"',`city_id`='"+Integer.parseInt(city_id)+"' WHERE `contact_number`='"+mobile+"';");
                    
                    JOptionPane.showMessageDialog(this, "Customer Updated Successfully!!!", "Success", JOptionPane.INFORMATION_MESSAGE);
                    
                    jTextField1.setEditable(false);
                    jTextField2.setEditable(false);
                    jComboBox1.setEnabled(false);

                }else{
                    
                    DBConnection.iud("INSERT INTO `customer`(`name`,`contact_number`,`city_id`) VALUES('"+cname+"','"+mobile+"','"+Integer.parseInt(city_id)+"');");
                    
                    JOptionPane.showMessageDialog(this, "Customer Registered Successfully!!!", "Success", JOptionPane.INFORMATION_MESSAGE);
                    
                    jTextField1.setEditable(false);
                    jTextField2.setEditable(false);
                    jComboBox1.setEnabled(false);
                    
                }
                
                jButton2.setEnabled(true);
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

