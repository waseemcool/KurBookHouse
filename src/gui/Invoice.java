    public void loadCities() {

        try {

            ResultSet rs = DBConnection.search("SELECT * FROM `city`;");

            Vector v = new Vector();
            v.add("Select");

            while (rs.next()) {
                v.add(rs.getString("name"));
            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }
    
    public void loadPaymentTypes(){
        
        try{
            
            ResultSet rs = DBConnection.search("SELECT * FROM `payment_type`;");
            
            Vector v = new Vector();
            v.add("Select");
            
            while(rs.next()){
                v.add(rs.getString("name"));
            }
            
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox2.setModel(dcm);
            
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }
    
    public void fullTotal(){
        
        double total = 0;

        for(int i = 0; i < jTable1.getRowCount(); i++){
            String t = jTable1.getValueAt(i, 7).toString();
            total = total + Double.parseDouble(t);
        }

        jLabel18.setText(df.format(total));
        
    }

