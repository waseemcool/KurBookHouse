    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String category = jComboBox1.getSelectedItem().toString();
        String publication = jComboBox2.getSelectedItem().toString();
        
        if(name.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter Product Name!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(category.equals("Select")){
            JOptionPane.showMessageDialog(this, "Please select a Category!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(publication.equals("Select")){
            JOptionPane.showMessageDialog(this, "Please select a Publication!!!", "Warning", JOptionPane.WARNING_MESSAGE);            
        }else{
            
            try {
                
                ResultSet rs = DBConnection.search("SELECT * FROM `product` INNER JOIN `category` ON `product`.`category_id`=`category`.`id` INNER JOIN `publication` ON `product`.`publication_id`=`publication`.`id` WHERE `product`.`name`='"+name+"' AND `category`.`name`='"+category+"' AND `publication`.`name`='"+publication+"';");
     
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "This Product Already Exists!!!!", "Warning", JOptionPane.WARNING_MESSAGE);
                }else{

                    ResultSet rs1 = DBConnection.search("SELECT `id` FROM `category` WHERE `name`='"+category+"';");
                    rs1.next();
                    
                    ResultSet rs2 = DBConnection.search("SELECT `id` FROM `publication` WHERE `name`='"+publication+"';");
                    rs2.next();

                    String category_id = rs1.getString("id");
                    String publication_id = rs2.getString("id");

                    DBConnection.iud("INSERT INTO `product`(`name`,`category_id`,`publication_id`) VALUES('"+name+"','"+category_id+"','"+publication_id+"');");

                    loadProducts();
                    
                    reset();

                    JOptionPane.showMessageDialog(this, "Product Registered Successfully!!!", "Success", JOptionPane.INFORMATION_MESSAGE);

                }
            
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

