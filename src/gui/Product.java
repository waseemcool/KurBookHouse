        String publication = jTable1.getValueAt(selectedRow, 3).toString();
        
        if(evt.getClickCount() == 2){
        
            if(selectedRow == -1){
                JOptionPane.showMessageDialog(this, "Please select a Product!!!", "Warning", JOptionPane.WARNING_MESSAGE);
            }else{
                
                if(grn != null){
                    
                    grn.jLabel10.setText(id);
                    grn.jLabel12.setText(name);
                    grn.jLabel14.setText(category);
                    grn.jLabel16.setText(publication);
                    
                    this.dispose();
                    
                }else{
                    
                    jLabel4.setText(id);
                    jTextField1.setText(name);
                    jComboBox1.setSelectedItem(category);                
                    jComboBox2.setSelectedItem(publication); 
                    jComboBox1.setEnabled(false);
                    jComboBox2.setEnabled(false);
                    jButton3.setEnabled(false);
                    
                }
                                
            }
            
        }
