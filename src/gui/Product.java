    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        // TODO add your handling code here:
        String search = jTextField2.getText();
        
        try {
            
            ResultSet rs = DBConnection.search("SELECT `product`.`id`,`product`.`name` AS `product_name`,`category`.`name` AS `category`,`publication`.`name` AS `publication` FROM `product` INNER JOIN `category` ON `product`.`category_id`=`category`.`id` INNER JOIN `publication` ON `product`.`publication_id`=`publication`.`id` WHERE `product`.`name` LIKE '%"+search+"%';");
            
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
                    
            while(rs.next()){
                
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("product_name"));
                v.add(rs.getString("category"));
                v.add(rs.getString("publication"));
                
                dtm.addRow(v);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField2KeyReleased

