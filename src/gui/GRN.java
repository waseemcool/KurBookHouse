        if (evt.getClickCount() == 2) {

            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select a GRN Item!!!", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                int option = JOptionPane.showConfirmDialog(this, "Do you want to Remove this GRN Item?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                if (option == JOptionPane.YES_OPTION) {

                    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                    dtm.removeRow(r);

                    fullTotal();

                    jLabel10.setText("None");
                    jLabel12.setText("None");
                    jLabel14.setText("None");
                    jLabel16.setText("None");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");

                    JOptionPane.showMessageDialog(this, "GRN Item Removed Successfully!!!", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            }

        }
