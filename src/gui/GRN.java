    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String paymentType = jComboBox1.getSelectedItem().toString();
        String payment = jTextField4.getText();

        if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Please add Products!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (paymentType.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a Payment Type!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (payment.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Payment!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("((0)|[1-9][0-9]*)|(([1-9][0-9]*)[.]([0]*[1-9][0-9]*))|([0][.]([0]*[1-9][0-9]*))").matcher(payment).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Payment!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            //GRN Insert
            long mTime = System.currentTimeMillis();

            String uniqueId = mTime + "-" + SignIn.userId;

            String sid = jLabel2.getText();

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String dNow = sdf.format(new Date());

            DBConnection.iud("INSERT INTO `grn`(`supplier_id`,`user_id`,`date_time`,`unique_id`) VALUES('" + sid + "','" + SignIn.userId + "','" + dNow + "','" + uniqueId + "');");
            //GRN Insert

            try {

                //GRN Payment Insert
                ResultSet rs = DBConnection.search("SELECT * FROM `grn` WHERE `unique_id`='" + uniqueId + "';");
                rs.next();
                String id = rs.getString("id");

                ResultSet rs2 = DBConnection.search("SELECT * FROM `payment_type` WHERE `name`='" + paymentType + "';");
                rs2.next();
                String paymentTypeId = rs2.getString("id");

                String balance = jLabel25.getText();

                DBConnection.iud("INSERT INTO `grn_payment`(`grn_id`,`payment_type_id`,`payment`,`balance`) VALUES('" + id + "','" + paymentTypeId + "','" + payment + "','" + balance + "');");
                //GRN Payment Insert

                String filepath = "src//reports//kbh_grn.jrxml";

                JasperReport jr = JasperCompileManager.compileReport(filepath);

                HashMap parameters = new HashMap();
                parameters.put("supplier", jLabel4.getText());
                parameters.put("mobile", jLabel6.getText());
                parameters.put("branch", jLabel8.getText());
                parameters.put("grn_id", id);

                //Stock Insert or Update Insert and GRN Item
                for (int i = 0; i < jTable1.getRowCount(); i++) {

                    String pid = jTable1.getValueAt(i, 0).toString();
                    String qty = jTable1.getValueAt(i, 4).toString();
                    String buyingPrice = jTable1.getValueAt(i, 5).toString();
                    String sellingPrice = jTable1.getValueAt(i, 6).toString();
                    //grn_id = id
                    //stock_id = ?

                    ResultSet rs3 = DBConnection.search("SELECT * FROM `stock` WHERE `product_id`='" + pid + "' AND `selling_price`='" + sellingPrice + "';");

                    {

                        String stock_id;

                        if (rs3.next()) {

                            //Update
                            stock_id = rs3.getString("id");
                            String stock_qty = rs3.getString("quantity");

                            int fullStockQty = Integer.parseInt(stock_qty) + Integer.parseInt(qty);

                            DBConnection.iud("UPDATE `stock` SET `quantity`='" + fullStockQty + "' WHERE `id`='" + stock_id + "';");
                            //Update

                        } else {

                            //Insert
                            DBConnection.iud("INSERT INTO `stock`(`product_id`,`quantity`,`selling_price`) VALUES('" + pid + "','" + qty + "','" + sellingPrice + "');");

                            ResultSet rs4 = DBConnection.search("SELECT * FROM `stock` WHERE `product_id`='" + pid + "' AND `selling_price`='" + sellingPrice + "';");
                            rs4.next();
                            stock_id = rs4.getString("id");

                        }

                        DBConnection.iud("INSERT INTO `grn_item`(`stock_id`,`quantity`,`buying_price`,`grn_id`) VALUES('" + stock_id + "','" + qty + "','" + buyingPrice + "','" + id + "');");
                        //Insert
                    }

                }
                //Stock Insert or Update Insert and GRN Item

                double full_total = Double.parseDouble(jLabel21.getText());
                double paid_payment = Double.parseDouble(payment);
                double balance_payment = Double.parseDouble(balance);

                parameters.put("full_total", full_total);
                parameters.put("payment_type", jComboBox1.getSelectedItem().toString());
                parameters.put("payment", paid_payment);
                parameters.put("balance", balance_payment);
                parameters.put("date_time", dNow);

                JRTableModelDataSource dataSource = new JRTableModelDataSource(jTable1.getModel());
                JasperPrint jp = JasperFillManager.fillReport(jr, parameters, dataSource);

                //Supplier
                jButton1.setEnabled(true);
                jButton1.setText("Select Supplier");
                jLabel2.setText("None");
                jLabel4.setText("None");
                jLabel6.setText("None");
                jLabel8.setText("None");
                //Supplier

                //Product
                jLabel10.setText("None");
                jLabel12.setText("None");
                jLabel14.setText("None");
                jLabel16.setText("None");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                //Product

                //payments
                jLabel21.setText("0.00");
                jComboBox1.setSelectedItem("Select");
                jTextField4.setText("");
                jTextField4.setEditable(false);
                jLabel25.setText("0.00");
                //payments

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);

                JOptionPane.showMessageDialog(this, "GRN Created Successfully!!!", "Success", JOptionPane.INFORMATION_MESSAGE);

                JasperViewer.viewReport(jp, false);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton5ActionPerformed

