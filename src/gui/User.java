    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String id = jLabel12.getText();
        String name = jTextField1.getText();
        String username = jTextField2.getText();
        String password = jPasswordField1.getText();
        String mobile = jTextField3.getText();
        String status = jComboBox1.getSelectedItem().toString();
        String type = jComboBox2.getSelectedItem().toString();
        String city = jComboBox3.getSelectedItem().toString();
        
        if (id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select a User!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Name!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(username.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter User Name!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter Password!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(mobile.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter Contact Number!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(!Pattern.compile("07[01245678][0-9]{7}").matcher(mobile).matches()){
            JOptionPane.showMessageDialog(this, "Invalid Contact Number!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(type.equals("Select")){
            JOptionPane.showMessageDialog(this, "Please select User Type!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(city.equals("Select")){
            JOptionPane.showMessageDialog(this, "Please select City!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            
            try{
                
                ResultSet rs1 = DBConnection.search("SELECT `id` FROM `user_status` WHERE `name`='"+status+"';");
                rs1.next();
                ResultSet rs2 = DBConnection.search("SELECT `id` FROM `user_type` WHERE `name`='"+type+"';");
                rs2.next();
                ResultSet rs3 = DBConnection.search("SELECT `id` FROM `city` WHERE `name`='"+city+"';");
                rs3.next();
                
                String status_id = rs1.getString("id");
                String type_id = rs2.getString("id");
                String city_id = rs3.getString("id");
            
                DBConnection.iud("UPDATE `user` SET `name`='"+name+"',`username`='"+username+"',`password`='"+password+"',`contact_number`='"+mobile+"',`user_status_id`='"+Integer.parseInt(status_id)+"',`user_type_id`='"+Integer.parseInt(type_id)+"',`city_id`='"+Integer.parseInt(city_id)+"' WHERE `id`='"+id+"';");
                
                reset();
                
                loadUsers();                
                
                JOptionPane.showMessageDialog(this, "User Account Updated Successfully!!!", "Success", JOptionPane.INFORMATION_MESSAGE);
                
            }catch (Exception e){
                e.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

