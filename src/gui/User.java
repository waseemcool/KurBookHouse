    public void loadUsers(){
        
        try {
            
            ResultSet rs = DBConnection.search("SELECT `user`.`id`,`user`.`name`,`user`.`username`,`user`.`contact_number`,`city`.`name` AS `city_name`,`user_status`.`name` AS `user_status_name`,`user_type`.`name` AS `user_type_name` FROM `user` INNER JOIN `user_status` ON `user`.`user_status_id`=`user_status`.`id` INNER JOIN `user_type` ON `user`.`user_type_id`=`user_type`.`id` INNER JOIN `city` ON `user`.`city_id`=`city`.`id`;");
            
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
                    
            while(rs.next()){
                
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("username"));
                v.add(rs.getString("contact_number"));                
                v.add(rs.getString("user_status_name"));
                v.add(rs.getString("user_type_name"));
                v.add(rs.getString("city_name"));
                
                dtm.addRow(v);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }

