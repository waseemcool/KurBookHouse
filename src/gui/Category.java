    public void loadCategories(){
        
        try{
            
            ResultSet rs = DBConnection.search("SELECT * FROM `category`;");
            
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()){
                
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                
                dtm.addRow(v);
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }

