    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();

        if(name.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter Category Name!!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{

            try {

                ResultSet rs = DBConnection.search("SELECT * FROM `category` WHERE `name`='"+name+"'");

                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "This Category Already Registered!!!", "Warning", JOptionPane.WARNING_MESSAGE);
                }else{

                    DBConnection.iud("INSERT INTO `category`(`name`) VALUES('"+name+"');");

                    loadCategories();
                    
                    this.p.loadCategories();
                    
                    jTextField1.setText("");
                    jTextField1.grabFocus();

                    JOptionPane.showMessageDialog(this, "Category Registered Successfully!!!", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

